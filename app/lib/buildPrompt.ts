import type { ResolvedConditions } from "../types";

export function buildPrompt(c: ResolvedConditions): string {
  return `あなたはプロのパーソナルトレーナーです。以下の条件に合う筋トレメニューを Markdown 形式で出力してください。

【条件】
- 目標: ${c.goal}
- 鍛えたい部位: ${c.parts.join("・")}
- 経験レベル: ${c.level}
- トレーニング時間: ${c.duration}
- 使用できる器具: ${c.equipment}

【出力フォーマット（厳守）】
以下の構造のみを出力すること。前置き・挨拶・自己紹介・「承知しました」「以下に〜」などの導入文、最後の総括や感想は**一切不要**。Markdown のセクションのみを出力する。

## ウォームアップ
（2〜3種目）
### 種目名
- セット数: 〇セット
- 回数/時間: 〇回 または 〇秒
- 休憩: 〇秒
- フォームのポイント: 簡潔に1〜2文

## メインセット
（4〜6種目、上と同じフォーマット）

## クールダウン
（1〜2種目、上と同じフォーマット）

## ワンポイントアドバイス
1〜2文の短いアドバイス（見出しは付けず本文のみ）。

【禁止事項】
- 「承知しました」「プロのパーソナルトレーナーとして」等の前置き
- 「頑張りましょう」等の励まし・まとめの追加段落
- 上記フォーマット以外の見出し（例: 「目標: 〇〇」のような重複表示）`;
}
